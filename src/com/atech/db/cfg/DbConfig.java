package com.atech.db.cfg;

public class DbConfig
{

    /** The jdbc_url. */
    public String jdbc_url = null;

    /** The jdbc_driver. */
    public String jdbc_driver = null;

    /** The jdbc_username. */
    public String jdbc_username = null;

    /** The jdbc_password. */
    public String jdbc_password = null;

    /**
     * Gets the jdbc_url.
     * 
     * @return the jdbc_url
     */
    public String getJdbc_url()
    {
        return jdbc_url;
    }

    /**
     * Sets the jdbc_url.
     * 
     * @param jdbc_url
     *            the new jdbc_url
     */
    public void setJdbc_url(String jdbc_url)
    {
        this.jdbc_url = jdbc_url;
    }

    /**
     * Gets the jdbc_driver.
     * 
     * @return the jdbc_driver
     */
    public String getJdbc_driver()
    {
        return jdbc_driver;
    }

    /**
     * Sets the jdbc_driver.
     * 
     * @param jdbc_driver
     *            the new jdbc_driver
     */
    public void setJdbc_driver(String jdbc_driver)
    {
        this.jdbc_driver = jdbc_driver;
    }

    /**
     * Gets the jdbc_username.
     * 
     * @return the jdbc_username
     */
    public String getJdbc_username()
    {
        return jdbc_username;
    }

    /**
     * Sets the jdbc_username.
     * 
     * @param jdbc_username new jdbc_username
     */
    public void setJdbc_username(String jdbc_username)
    {
        this.jdbc_username = jdbc_username;
    }

    /**
     * Gets the jdbc_password.
     * 
     * @return the jdbc_password
     */
    public String getJdbc_password()
    {
        return jdbc_password;
    }

    /**
     * Sets the jdbc_password.
     * 
     * @param jdbc_password new jdbc_password
     */
    public void setJdbc_password(String jdbc_password)
    {
        this.jdbc_password = jdbc_password;
    }

}
